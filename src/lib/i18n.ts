export const i18n = createI18n({
  legacy: false,
  locale: 'zh-CN',
  fallbackLocale: 'zh-CN',
  messages,
})

export const appLocaleRef = i18n.global.locale

export function setAppLocale(locale: LocalePreference) {
  i18n.global.locale.value = locale
}

export function translate(key: string, values?: Record<string, unknown>) {
  return (values
    ? i18n.global.t(key, values)
    : i18n.global.t(key)) as string
}

export function formatAppDate(value: string | number | Date) {
  return new Intl.DateTimeFormat(appLocaleRef.value).format(new Date(value))
}

export function formatAppTime(value: string | number | Date) {
  return new Intl.DateTimeFormat(appLocaleRef.value, {
    hour: 'numeric',
    minute: '2-digit',
    second: '2-digit',
  }).format(new Date(value))
}
